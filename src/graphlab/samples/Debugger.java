package graphlab.samples;

/**
 * This is a sample class for who wants to debug his/her developing extensions.
 * Just replace your extension's class name with HelloWorldReport
 *
 * see: http://graphlab.sharif.edu/trac/wiki/ExtensionDebugging
 * @author Azin Azadi
 */

import graphlab.platform.Application;
import graphlab.platform.StaticUtils;
import graphlab.samples.extensions.HelloWorldReport;


public class Debugger {
    public static void main(String[] args) {
        graphlab.platform.Application.main(args);
        StaticUtils.loadSingleExtension(graphlab.samples.extensions.HelloWorldReport.class);
    }
}
